def draw():
    window.fill((31, 36, 37))
    # draw a big title
    text = font3.render('HANGMAN', 1, (255, 255, 255)) # title
    window.blit(text , (WIDTH / 2 - text.get_width() / 2 , 10))  #copying the text surface object to the display surface object at the center coordinate

    # Draw secret_word
    display_word = " "          # this is like a counter
    for letter in secret_word:
        if letter in guessed_letter:
            display_word += letter + " "    # once we guess the word  , and if the guessed letter is correct , then we no longer need the '_' so this removes the underscore and adds the letter we entered
