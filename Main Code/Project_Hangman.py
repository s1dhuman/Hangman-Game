def main_loop():
    global hangman_initial_status
    global fps
    # game loop
    run = True
    while run:
        clock.tick(fps)
        for event in pygame.event.get():  # whatever event is done it is getting is stored inside this place
            if event.type == pygame.QUIT:  # if type of event we're doing is clicking 'X' then
                run = False  # the program no more runs
            if event.type == pygame.MOUSEBUTTONDOWN:
                mouse_x , mouse_y = pygame.mouse.get_pos()  # tells the position of cursor
                # we have to see that whenever we click on circle that particular thing is selected

                for letter in letters:
                    x , y , ltr , visible = letter
                    # now we have to find the distance of position of mouse and position of letter
                    # it doesnt matter if its x1 - mouse_x or x1
                    if visible:
                        distance = math.sqrt((x - mouse_x) ** 2 + (y - mouse_y) ** 2)  # pythagoras theorem
                        if distance < radius:
                            letter[3] = False
                            guessed_letter.append((ltr))  # add the guessed letter to letter
                            if ltr not in secret_word:  # if guessed letter is not in the secret_word
                                hangman_initial_status += 1  # the draw the next hangman figure
        draw()
        won = True
        for letter in secret_word:  # to check for every letter in the secret_word (hidden one)
            if letter not in guessed_letter:
                won = False
        if won:
            won_or_lost_message('hehe you nailed it')
            print('the secret word was: ', secret_word)
            break

        if hangman_initial_status == 6:  # once all the images are finished and man hangs
            won_or_lost_message('Sorry, you lost Better luck next time')
            print('The secret_word was:', secret_word)
            break
        
        
# -----------------------------------------------------------------------------------

main_loop()
